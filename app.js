// Create enhanced event card with proper event binding
function createEventCard(event, category, index) {
    const card = document.createElement('div');
    card.className = 'event-card';
    card.innerHTML = `<h3>${event.name}</h3>`;
    
    // Set data attributes for debugging
    card.setAttribute('data-event-id', event.id);
    card.setAttribute('data-category', category);
    card.setAttribute('data-event-name', event.name);
    
    // Store a reference to the original event data (not a copy to avoid issues)
    card.eventData = event;
    
    // Add staggered animation delay for entrance
    card.style.animationDelay = `${index * 0.1}s`;
    
    // Enhanced click event with proper event binding and debugging
    card.addEventListener('click', function(e) {
        e.preventDefault();
        e.stopPropagation();
        
        console.log(`🎯 Event card clicked: "${event.name}" (ID: ${event.id})`);
        console.log('Event data:', event);
        
        // Add click visual feedback
        this.style.transform = 'scale(0.95)';
        
        setTimeout(() => {
            this.style.transform = '';
            // Use the event data directly from closure
            openEventModal(event);
        }, 150);
    });

    // Enhanced hover effects
    card.addEventListener('mouseenter', function(e) {
        this.style.transform = 'translateY(-8px) scale(1.02)';
        this.style.boxShadow = '0 15px 40px rgba(255, 107, 53, 0.3), 0 5px 15px rgba(0, 0, 0, 0.2)';
    });
    
    card.addEventListener('mouseleave', function(e) {
        this.style.transform = '';
        this.style.boxShadow = '';
    });

    // Add ripple effect on click
    card.addEventListener('mousedown', function(e) {
        createRippleEffect(e, this);
    });

    // Ensure the card is interactive
    card.style.cursor = 'pointer';
    card.style.userSelect = 'none';

    console.log(`✅ Event card created for: ${event.name} (${category})`);
    return card;
}

// Create spectacular ripple effect
function createRippleEffect(event, element) {
    const ripple = document.createElement('span');
    const rect = element.getBoundingClientRect();
    const size = Math.max(rect.width, rect.height);
    const x = event.clientX - rect.left - size / 2;
    const y = event.clientY - rect.top - size / 2;
    
    ripple.style.cssText = `
        position: absolute;
        width: ${size}px;
        height: ${size}px;
        left: ${x}px;
        top: ${y}px;
        background: radial-gradient(circle, rgba(255, 107, 53, 0.4) 0%, transparent 70%);
        border-radius: 50%;
        transform: scale(0);
        animation: rippleExpand 0.6s linear;
        pointer-events: none;
        z-index: 10;
    `;
    
    element.style.position = 'relative';
    element.appendChild(ripple);
    
    setTimeout(() => {
        if (ripple.parentNode) {
            ripple.remove();
        }
    }, 600);
}

// Open event detail modal with enhanced animations and proper data handling
function openEventModal(event) {
    console.log(`🚀 Opening modal for event: "${event.name}" (ID: ${event.id})`);
    console.log('Modal event data:', event);
    
    if (!event || !event.name || !event.description) {
        console.error('❌ Invalid event data provided:', event);
        return;
    }
    
    // Store current event globally
    currentEvent = event;
    
    const eventModal = document.getElementById('eventModal');
    const modalEventName = document.getElementById('modalEventName');
    const modalEventDescription = document.getElementById('modalEventDescription');
    const modalEventGuidelines = document.getElementById('modalEventGuidelines');
    
    if (!eventModal || !modalEventName || !modalEventDescription || !modalEventGuidelines) {
        console.error('❌ Modal elements not found');
        return;
    }
    
    // Populate modal content with the correct event data
    modalEventName.textContent = event.name;
    modalEventDescription.textContent = event.description;
    
    // Clear and populate guidelines with enhanced styling
    modalEventGuidelines.innerHTML = '';
    
    if (event.guidelines && Array.isArray(event.guidelines) && event.guidelines.length > 0) {
        event.guidelines.forEach((guideline, index) => {
            const li = document.createElement('li');
            li.textContent = guideline;
            li.style.animationDelay = `${index * 0.1}s`;
            li.classList.add('guideline-item');
            modalEventGuidelines.appendChild(li);
        });
    } else {
        // Add fallback if no guidelines
        const li = document.createElement('li');
        li.textContent = 'Guidelines will be provided during event registration.';
        li.classList.add('guideline-item');
        modalEventGuidelines.appendChild(li);
    }

    // Update register button based on registration link availability
    updateRegisterButton(event.registerLink);

    // Show modal with spectacular entrance
    eventModal.classList.remove('hidden');
    eventModal.style.display = 'flex';
    eventModal.style.opacity = '1';
    eventModal.style.visibility = 'visible';
    
    // Prevent background scrolling
    document.body.style.overflow = 'hidden';
    
    // Add modal entrance animation
    const modalContent = eventModal.querySelector('.modal-content');
    if (modalContent) {
        modalContent.style.transform = 'scale(0.8) translateY(20px) rotateX(15deg)';
        modalContent.style.opacity = '0';
        
        requestAnimationFrame(() => {
            modalContent.style.transition = 'all 0.4s cubic-bezier(0.175, 0.885, 0.32, 1.275)';
            modalContent.style.transform = 'scale(1) translateY(0) rotateX(0deg)';
            modalContent.style.opacity = '1';
        });
    }
    
    console.log(`✨ Modal successfully opened for: ${event.name}`);
}

// Update register button based on link availability
function updateRegisterButton(registerLink) {
    const registerBtn = document.getElementById('registerBtn');
    
    if (!registerBtn) {
        console.error('❌ Register button not found');
        return;
    }
    
    if (registerLink && registerLink.trim() !== '') {
        // Enable button for valid links
        registerBtn.textContent = 'Register Now';
        registerBtn.disabled = false;
        registerBtn.classList.remove('btn--disabled');
        registerBtn.classList.add('btn--primary');
        registerBtn.style.cursor = 'pointer';
        registerBtn.style.opacity = '1';
        
        console.log(`✅ Register button enabled with link: ${registerLink}`);
    } else {
        // Disable button when no link available
        registerBtn.textContent = 'Link Coming Soon';
        registerBtn.disabled = true;
        registerBtn.classList.add('btn--disabled');
        registerBtn.classList.remove('btn--primary');
        registerBtn.style.cursor = 'not-allowed';
        registerBtn.style.opacity = '0.6';
        
        console.log('⏳ Register button disabled - no link available');
    }
}

// Close modal with enhanced exit animation
function closeEventModal() {
    console.log('🚪 Closing event modal...');
    const eventModal = document.getElementById('eventModal');
    
    if (!eventModal) {
        console.error('❌ Modal element not found');
        return;
    }
    
    const modalContent = eventModal.querySelector('.modal-content');
    
    // Exit animation
    if (modalContent) {
        modalContent.style.transition = 'all 0.3s ease-in-out';
        modalContent.style.transform = 'scale(0.9) translateY(20px)';
        modalContent.style.opacity = '0';
    }
    
    setTimeout(() => {
        eventModal.classList.add('hidden');
        eventModal.style.display = 'none';
        eventModal.style.opacity = '0';
        eventModal.style.visibility = 'hidden';
        
        // Restore scrolling
        document.body.style.overflow = '';
        
        // Reset modal content
        if (modalContent) {
            modalContent.style.transform = '';
            modalContent.style.opacity = '';
            modalContent.style.transition = '';
        }
        
        currentEvent = null;
    }, 300);
    
    console.log('✅ Modal closed successfully');
}

// Enhanced register button functionality
function handleRegisterClick() {
    if (!currentEvent) {
        console.error('❌ No current event selected');
        return;
    }
    
    const registerLink = currentEvent.registerLink;
    const eventName = currentEvent.name;
    
    console.log(`📝 Register button clicked for: ${eventName}`);
    console.log(`Registration link: ${registerLink}`);
    
    if (registerLink && registerLink.trim() !== '') {
        // Open the registration link in a new tab
        try {
            const newWindow = window.open(registerLink, '_blank', 'noopener,noreferrer');
            if (newWindow) {
                console.log(`✅ Opened registration form in new tab: ${registerLink}`);
                
                // Provide user feedback
                const registerBtn = document.getElementById('registerBtn');
                if (registerBtn) {
                    const originalText = registerBtn.textContent;
                    registerBtn.textContent = 'Opening Form...';
                    registerBtn.style.background = 'linear-gradient(45deg, #4CAF50, #45a049)';
                    
                    setTimeout(() => {
                        registerBtn.textContent = originalText;
                        registerBtn.style.background = '';
                    }, 2000);
                }
            } else {
                console.warn('⚠️ Pop-up blocked - showing fallback message');
                alert(`Registration form link:\n${registerLink}\n\nPlease copy this link and open it in a new tab to register for ${eventName}.`);
            }
        } catch (error) {
            console.error('❌ Error opening registration link:', error);
            alert(`Please visit this link to register:\n${registerLink}`);
        }
    } else {
        console.log('⏳ Registration link not available yet');
        // Button should be disabled, but show message if somehow clicked
        alert(`Registration for "${eventName}" will be available soon!\n\nPlease check back later for the registration link.`);
    }
}

// Setup enhanced event listeners
function setupEventListeners() {
    console.log('🎛️ Setting up event listeners...');
    
    const closeModal = document.getElementById('closeModal');
    const eventModal = document.getElementById('eventModal');
    const registerBtn = document.getElementById('registerBtn');
    
    // Close modal button with enhanced functionality
    if (closeModal) {
        closeModal.addEventListener('click', function(e) {
            e.preventDefault();
            e.stopPropagation();
            console.log('❌ Close button clicked');
            closeEventModal();
        });
        
        // Add hover effect to close button
        closeModal.addEventListener('mouseenter', function() {
            this.style.transform = 'scale(1.1) rotate(90deg)';
            this.style.background = 'rgba(255, 107, 53, 0.2)';
        });
        
        closeModal.addEventListener('mouseleave', function() {
            this.style.transform = 'scale(1) rotate(0deg)';
            this.style.background = '';
        });
    }
    
    // Close modal on background click
    if (eventModal) {
        eventModal.addEventListener('click', function(e) {
            if (e.target === eventModal) {
                console.log('🖱️ Background clicked - closing modal');
                closeEventModal();
            }
        });
    }

    // Close modal with Escape key
    document.addEventListener('keydown', function(e) {
        if (e.key === 'Escape' && eventModal && !eventModal.classList.contains('hidden')) {
            console.log('⌨️ Escape key pressed - closing modal');
            closeEventModal();
        }
    });

    // Enhanced register button with new functionality
    if (registerBtn) {
        registerBtn.addEventListener('click', function(e) {
            e.preventDefault();
            e.stopPropagation();
            
            // Only proceed if button is not disabled
            if (!this.disabled) {
                // Add visual feedback
                this.style.transform = 'scale(0.95)';
                
                setTimeout(() => {
                    this.style.transform = 'scale(1)';
                    handleRegisterClick();
                }, 150);
            }
        });
        
        // Add enhanced hover effects to register button (only when not disabled)
        registerBtn.addEventListener('mouseenter', function() {
            if (!this.disabled) {
                this.style.boxShadow = '0 0 20px rgba(255, 107, 53, 0.6)';
                this.style.transform = 'translateY(-2px)';
            }
        });
        
        registerBtn.addEventListener('mouseleave', function() {
            if (!this.disabled) {
                this.style.boxShadow = '';
                this.style.transform = '';
            }
        });
    }

    // Add smooth scrolling
    document.documentElement.style.scrollBehavior = 'smooth';
    
    // Add performance optimization styles
    addPerformanceOptimizations();
    
    console.log('✅ All event listeners configured successfully!');
}

