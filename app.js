// Setup enhanced event listeners
function setupEventListeners() {
    console.log('ðŸŽ›ï¸ Setting up event listeners...');
    
    const closeModal = document.getElementById('closeModal');
    const eventModal = document.getElementById('eventModal');
    const registerBtn = document.getElementById('registerBtn');
    
    // Close modal button with enhanced functionality
    if (closeModal) {
        closeModal.addEventListener('click', function(e) {
            e.preventDefault();
            e.stopPropagation();
            console.log('âŒ Close button clicked');
            closeEventModal();
        });
        
        // Add hover effect to close button
        closeModal.addEventListener('mouseenter', function() {
            this.style.transform = 'scale(1.1) rotate(90deg)';
            this.style.background = 'rgba(255, 107, 53, 0.2)';
        });
        
        closeModal.addEventListener('mouseleave', function() {
            this.style.transform = 'scale(1) rotate(0deg)';
            this.style.background = '';
        });
    }
    
    // Close modal on background click
    if (eventModal) {
        eventModal.addEventListener('click', function(e) {
            if (e.target === eventModal) {
                console.log('ðŸ–±ï¸ Background clicked - closing modal');
                closeEventModal();
            }
        });
    }

    // Close modal with Escape key
    document.addEventListener('keydown', function(e) {
        if (e.key === 'Escape' && eventModal && !eventModal.classList.contains('hidden')) {
            console.log('âŒ¨ï¸ Escape key pressed - closing modal');
            closeEventModal();
        }
    });

    // Enhanced register button with new functionality
    if (registerBtn) {
        registerBtn.addEventListener('click', function(e) {
            e.preventDefault();
            e.stopPropagation();
            
            // Only proceed if button is not disabled
            if (!this.disabled) {
                // Add visual feedback
                this.style.transform = 'scale(0.95)';
                
                setTimeout(() => {
                    this.style.transform = 'scale(1)';
                    handleRegisterClick();
                }, 150);
            }
        });
        
        // Add enhanced hover effects to register button (only when not disabled)
        registerBtn.addEventListener('mouseenter', function() {
            if (!this.disabled) {
                this.style.boxShadow = '0 0 20px rgba(255, 107, 53, 0.6)';
                this.style.transform = 'translateY(-2px)';
            }
        });
        
        registerBtn.addEventListener('mouseleave', function() {
            if (!this.disabled) {
                this.style.boxShadow = '';
                this.style.transform = '';
            }
        });
    }

    // Add smooth scrolling
    document.documentElement.style.scrollBehavior = 'smooth';
    
    // Add performance optimization styles
    addPerformanceOptimizations();
    
    console.log('âœ… All event listeners configured successfully!');
}

