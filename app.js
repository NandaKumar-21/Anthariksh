// Create enhanced event card with proper event binding
function createEventCard(event, category, index) {
    const card = document.createElement('div');
    card.className = 'event-card';
    card.innerHTML = `<h3>${event.name}</h3>`;
    
    // Set data attributes for debugging
    card.setAttribute('data-event-id', event.id);
    card.setAttribute('data-category', category);
    card.setAttribute('data-event-name', event.name);
    
    // Store a reference to the original event data (not a copy to avoid issues)
    card.eventData = event;
    
    // Add staggered animation delay for entrance
    card.style.animationDelay = `${index * 0.1}s`;
    
    // Enhanced click event with proper event binding and debugging
    card.addEventListener('click', function(e) {
        e.preventDefault();
        e.stopPropagation();
        
        console.log(`ðŸŽ¯ Event card clicked: "${event.name}" (ID: ${event.id})`);
        console.log('Event data:', event);
        
        // Add click visual feedback
        this.style.transform = 'scale(0.95)';
        
        setTimeout(() => {
            this.style.transform = '';
            // Use the event data directly from closure
            openEventModal(event);
        }, 150);
    });

    // Enhanced hover effects
    card.addEventListener('mouseenter', function(e) {
        this.style.transform = 'translateY(-8px) scale(1.02)';
        this.style.boxShadow = '0 15px 40px rgba(255, 107, 53, 0.3), 0 5px 15px rgba(0, 0, 0, 0.2)';
    });
    
    card.addEventListener('mouseleave', function(e) {
        this.style.transform = '';
        this.style.boxShadow = '';
    });

    // Add ripple effect on click
    card.addEventListener('mousedown', function(e) {
        createRippleEffect(e, this);
    });

    // Ensure the card is interactive
    card.style.cursor = 'pointer';
    card.style.userSelect = 'none';

    console.log(`âœ… Event card created for: ${event.name} (${category})`);
    return card;
}

// Create spectacular ripple effect
function createRippleEffect(event, element) {
    const ripple = document.createElement('span');
    const rect = element.getBoundingClientRect();
    const size = Math.max(rect.width, rect.height);
    const x = event.clientX - rect.left - size / 2;
    const y = event.clientY - rect.top - size / 2;
    
    ripple.style.cssText = `
        position: absolute;
        width: ${size}px;
        height: ${size}px;
        left: ${x}px;
        top: ${y}px;
        background: radial-gradient(circle, rgba(255, 107, 53, 0.4) 0%, transparent 70%);
        border-radius: 50%;
        transform: scale(0);
        animation: rippleExpand 0.6s linear;
        pointer-events: none;
        z-index: 10;
    `;
    
    element.style.position = 'relative';
    element.appendChild(ripple);
    
    setTimeout(() => {
        if (ripple.parentNode) {
            ripple.remove();
        }
    }, 600);
}

