// Add dynamic particle effects for enhanced visual impact
function addDynamicParticles() {
    const particleContainer = document.querySelector('.particles');
    
    if (!particleContainer) return;
    
    // Add more particles for spectacular effect
    for (let i = 0; i < 15; i++) {
        const particle = document.createElement('div');
        particle.className = 'particle';
        particle.style.setProperty('--delay', `${Math.random() * 2}s`);
        particle.style.setProperty('--x', `${Math.random() * 100}%`);
        particle.style.setProperty('--y', `${70 + Math.random() * 20}%`);
        
        // Random colors for particles
        const colors = ['#FF6B35', '#FFD700', '#4A90E2', '#FF4444'];
        particle.style.backgroundColor = colors[Math.floor(Math.random() * colors.length)];
        
        particleContainer.appendChild(particle);
    }
}

// Animate main content entrance
